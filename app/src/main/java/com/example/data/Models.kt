data class BloodDonor(
    val id: String,
    val name: String,
    val bloodGroup: String,
    val phone: String,
    val email: String,
    val district: String,
    val upazila: String,
    val lastDonationDate: String, // e.g., "2026-03-12" or "Available"
    val isAvailable: Boolean = true,
    val isApproved: Boolean = true,
    val donationCount: Int = 0,
    val isGoogleUser: Boolean = false,
    val country: String = "Bangladesh",
    val userId: String = "",
    val isWarning: Boolean = false,
    val warningReason: String = "",
    val role: String = "Donor" // "Donor" or "Requester"
) {
    val displayUserId: String
        get() = if (userId.isNotBlank()) userId else "ABB-${(id.hashCode().let { if (it < 0) -it else it } % 90000 + 10000)}"
}

data class BloodRequest(
    val id: String,
    val patientName: String,
    val bloodGroup: String,
    val bloodAmount: String = "",
    val hospitalName: String,
    val district: String,
    val upazila: String,
    val contactNumber: String,
    val details: String = "Urgent blood needed, please contact immediately.",
    val isEmergency: Boolean = true,
    val isApproved: Boolean = true,
    val dateRequested: String = "2026-06-12",
    val status: String = "Active", // Active, Completed
    val country: String = "Bangladesh",
    val patientGender: String = "Male",
    val medicalCondition: String = ""
)

data class DonationNotification(
    val id: String,
    val titleEn: String,
    val titleBn: String,
    val messageEn: String,
    val messageBn: String,
    val timestamp: String,
    val isRead: Boolean = false,
    val type: String = "ALERT", // ALERT, REQUEST, SUCCESS
    val country: String = "Bangladesh"
)

data class ScamReport(
    val id: String,
    val reporterName: String,
    val reporterPhone: String,
    val scammerDonorId: String,
    val scammerDonorName: String,
    val scammerDonorPhone: String,
    val reason: String,
    val amountDemanded: String,
    val timestamp: String,
    val status: String = "Pending", // Pending, Dismissed, Banned
    val country: String = "Bangladesh",
    val scammerPhotoUri: String? = null
)

data class ChatMessage(
    val id: String,
    val senderPhone: String,
    val senderName: String,
    val receiverPhone: String,
    val receiverName: String,
    val message: String,
    val timestamp: String,
    val isRead: Boolean = false
)

data class Ambulance(
    val id: String,
    val ownerName: String,
    val serviceName: String,
    val phone: String,
    val district: String,
    val upazila: String,
    val ambulanceType: String, // AC, Non-AC, Freezer, ICU
